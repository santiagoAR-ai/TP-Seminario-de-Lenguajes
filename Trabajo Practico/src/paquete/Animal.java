package paquete;

public class Animal {
private String especie;
private String nombre;
private int edad;
private int peso;
private int tamaño;
public Animal(String especie) {
	this.especie=especie;
}
public String getespecie() {
	return especie;
}
public String getnombre() {
	return nombre;
}
public int getedad() {
	return edad;
}
public int getpeso() {
	return peso;
}
public int gettamaño() {
	return tamaño;
}
}
